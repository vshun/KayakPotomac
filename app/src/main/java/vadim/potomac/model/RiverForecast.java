package vadim.potomac.model;

public class RiverForecast {

	private String date;
	private String level;
	
	public void setDate(String date) {
		this.date = date;
	}

	public void setLevel(String level) {
		this.level = level;
	}
	
	public String getDate() {
		return date;
	}

	public String getLevel() {
		return level;
	}	
	
}
